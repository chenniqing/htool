package cn.javaex.htool.core.date.constant;

/**
 * 日期格式化常量
 *
 * @author 陈霓清
 * @Date 2022年11月24日
 */
public class DatePattern {
    /**
     * 2022-11-24
     */
    public static final String yyyyMMdd = "yyyy-MM-dd";
    /**
     * 2022-11-24 23
     */
    public static final String yyyyMMddHH = "yyyy-MM-dd HH";
    /**
     * 2022-11-24 23:59
     */
    public static final String yyyyMMddHHmm = "yyyy-MM-dd HH:mm";
    /**
     * 2022-11-24 23:59:59
     */
    public static final String yyyyMMddHHmmss = "yyyy-MM-dd HH:mm:ss";

    /**
     * 2022年11月24日
     */
    public static final String yyyy年MM月dd日 = "yyyy年MM月dd日";
    /**
     * 2022年11月24日 23时
     */
    public static final String yyyy年MM月dd日HH时 = "yyyy年MM月dd日 HH时";
    /**
     * 2022年11月24日 23时59分
     */
    public static final String yyyy年MM月dd日HH时mm分 = "yyyy年MM月dd日 HH时mm分";
    /**
     * 2022年11月24日 23时59分59秒
     */
    public static final String yyyy年MM月dd日HH时mm分ss秒 = "yyyy年MM月dd日 HH时mm分ss秒";

}
